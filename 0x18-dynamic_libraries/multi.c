#include "100-operations.h"

/**
 * multi - Multiply two numbers
 * @a: First number
 * @b: Second number
 *
 * Description: This function multiplies two numbers.
 * Return: The result of the multiplication.
 */

size_t multi(size_t a, size_t b)
{
	size_t result = 0;

	result = a * b;
	return (result);
}
